#include <stdio.h>
/**
 * add - adding int a and int b
 * @a: first int
 * @b: second int
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * mul - multiplying int a and int b
 * @a: first int
 * @b: second int
 * Return: Multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - dividing int a and int b
 * @a: first int
 * @b: second int
 * Return: Quotient
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * sub - subtracting int a and int b
 * @a: first int
 * @b: second int
 * Return: sub
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mod - remainder of a % b
 * @a: first int
 * @b: second int
 * Return: remainder
 */
int mod(int a, int b)
{
	return (a % b);
}
